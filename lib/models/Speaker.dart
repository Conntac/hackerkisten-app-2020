class Speaker{
  String name;
  String company;
  String imageURL;
  String talkTitle;
  String talkDescription;

  Speaker(name, company, imageURL, talkTitle, talkDescription) {
    this.name = name;
    this.company = company;
    this.imageURL = imageURL;
    this.talkTitle = talkTitle;
    this.talkDescription = talkDescription;
  }
}